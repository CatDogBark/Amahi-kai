require 'shell'
require 'shellwords'

class DnsAlias < ApplicationRecord

  after_save :restart
  after_destroy :restart

  scope :user_visible,->{where(["address != ?", ''])}

  validates :name, presence: true, uniqueness: true, format: { with: /\A[a-z][a-z0-9-]*\z/i }

  protected

  def restart
    regenerate_dnsmasq_config
    reload_dnsmasq
  end

  private

  def regenerate_dnsmasq_config
    lines = ["# Auto-generated by Amahi-kai â€” do not edit manually"]
    DnsAlias.all.each do |a|
      if a.address.present?
        lines << "address=/#{a.name}/#{a.address}"
      else
        lines << "address=/#{a.name}/"
      end
    end

    FileUtils.mkdir_p(HDA_TMP_DIR)
    staged_file = File.join(HDA_TMP_DIR, "amahi-aliases.conf")
    File.write(staged_file, lines.join("\n") + "\n")

    Shell.run("cp #{Shellwords.escape(staged_file)} /etc/dnsmasq.d/amahi-aliases.conf")
  end

  def reload_dnsmasq
    Shell.run("systemctl restart dnsmasq.service")
  end

end
